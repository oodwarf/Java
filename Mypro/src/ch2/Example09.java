package ch2;

public class Example09 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int number1=12;
		int number2=21;
		System.out.println("12>21:"+(number1>number2));//false
		System.out.println("12<21:"+(number1<number2));//true
		System.out.println("12==21:"+(number1==number2));//false
		System.out.println("12!=21:"+(number1!=number2));//true
		System.out.println("12>=21:"+(number1>=number2));//false
		System.out.println("12<=21:"+(number1<=number2));//true
	}

}

