package ch4;

public class Example02 {
//数组静态初始化
	//数据类型 [] 数组名={v1,v2,v3,...,vn};
	public static void main(String[] args) {
		int arr[]={1,2,3,4};
		System.out.println("arr[0]="+arr[0]);
		System.out.println("arr[1]="+arr[1]);
		System.out.println("arr[2]="+arr[2]);
		System.out.println("arr[3]="+arr[3]);
	}

}